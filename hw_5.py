# Импортируем библиотеку random для генерации случайных чисел
import random
# Запускаем бесконечный цикл для ввода пользователем количества пословиц и вывода результатов
while True:
    user_input = input("Сколько пословиц вы хотите получить? ")
    # Проверяем, что пользователь ввел число и оно больше нуля
    if not user_input.isdigit() or int(user_input) <= 0:
        print("Введите корректное число!")
        continue
    # Преобразуем введенное значение в целое число
    user_input = int(user_input)
    # Проверяем, что введенное число не больше количества пословиц в списке
    if user_input > len(proverbs):
        print(f"Вы ввели число больше, чем есть пословиц в списке. В списке {len(proverbs)} пословиц.")
        continue
    # Создаем список для хранения результатов
    results = []
    # Запускаем цикл для создания пословиц
    while len(results) < user_input and proverbs and variants:
        # Выбираем и сразу удаляем случайную пословицу
        proverb = proverbs.pop(random.randint(0, len(proverbs) - 1)).lower()
        
        # Выбираем и сразу удаляем случайный вариант
        variant = variants.pop(random.randint(0, len(variants) - 1))
        # Заменяем "ум" на случайный вариант
        proverb = proverb.replace("ум", variant)
        # Добавляем пословицу в список результатов
        results.append(proverb)
    # Выводим результаты
    print(results)
    print(f"Создано пословиц: {len(results)}")
    # Запрашиваем у пользователя команду для продолжения или выхода
    command = input("Хотите продолжить, нажмите 'Enter'. Введите 'стоп' для выхода: ")
    # Если пользователь ввел 'стоп', выходим из цикла
    if command.lower() == 'стоп':
        print("Выход из программы.")
        break
    # Если закончились пословицы или варианты, выводим сообщение и выходим из цикла
    else:
        if not proverbs or not variants:
            print("Списки пословиц и вариантов закончились. Пословицы не созданы. Перезапустите программу и попробуйте снова.")
            break